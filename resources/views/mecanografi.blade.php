    Cuando hablamos de programacion, lo primero que se nos viene a la cabeza, es una maquina, o tal vez una computadora, o algo parecido, pero...
    si te dijera que podemos programar nuestros pensamientos y asi conseguir resultados diferentes.
    Abandonar la repetitiva y rutinaria catarata de pensamientos intuitivos que acarreamos desde nuestro nacimiento.



    La programación neurolingüística es una forma de cambiar los pensamientos y hábitos de una persona
    por medio de técnicas de percepción, comportamiento y comunicación. Es un enfoque pseudocientífico basado en conexiones neuronales, específicamente, en cómo procesan el lenguaje.


      SUB-TITULO
    ¿Cuáles son los principios básicos de la programación neurolingüística?
    Dentro de la programación neurolingüística, existen ocho principios básicos que ayudan en el cotidiano de las personas.


    Cada uno de estos principios, aplicados correctamente, ayudan en las acciones diarias, aumentando las posibilidades de alcanzar objetivos y optimizando los resultados en el trabajo y la vida personal.



    Rapport
    El rapport es uno de los principios básicos de la PNL. Crear un rapport con una persona durante la comunicación es, según la neurolingüística, una forma de fortalecer los lazos y garantizar cada vez más seguridad y tranquilidad en esa relación.

    El rapport no es más que crear una relación con el oyente, basada en refuerzos positivos que garantizan la confianza.

    También es necesario analizar comportamientos, ideas, argumentos y otros detalles durante la comunicación que, si se trabajan correctamente, pueden aumentar la aproximación con tu audiencia.


    Comportamiento
    La forma en que nos comportamos no define quiénes somos. Nuestros comportamientos son, en general, una reacción que tenemos ante las adversidades y las situaciones positivas en nuestro día a día.

    Esta reacción puede variar, incluso según el estado de ánimo y el día en que sucedan las cosas.

    En la programación neurolingüística, la clave es, por lo tanto, saber cambiar tu comportamiento y controlarlo en diferentes situaciones, asegurar que tus reacciones se realicen con inteligencia emocional y mejorar la forma en que enfrentas la adversidad.





    Intención positiva
    La intención positiva es un principio que desarrolla la idea de que detrás de todo comportamiento hay una intención positiva.

    En este sentido, la PNL entiende que todas las personas actúan generalmente con buenas intenciones, siempre haciendo lo mejor que pueden dentro de las posibilidades y con las herramientas y atribuciones que tienen a su disposición.

    De esa manera, si consideramos que las personas siempre actúan con buenas intenciones, es fundamental empatizar y no culparlas por los posibles resultados negativos. No debemos sentirnos frustrados, tristes o enojados por esta razón, puesto que cada uno hace lo mejor que puede.

    Lo ideal, en este punto, es tratar de entender que, incluso si el resultado no es satisfactorio, las personas hicieron lo mejor en ese momento.

    3 key pillars to drive th foundation, digital adaption




    Mapa mental
    Cada individuo es único, así como su manera de ver y comprender el mundo y los eventos que los rodean.

    Esto se debe a que, en cierto modo, no vemos las cosas como realmente son, sino de acuerdo con los "mapas mentales" que hemos construido a lo largo del tiempo, basados ​​en el conocimiento, las experiencias y las percepciones que se están transformando a medida que vivimos.

    Por lo tanto, a medida que crecemos y tenemos nuevas experiencias, actualizamos y cambiamos nuestros mapas mentales, transformando nuestra forma de reaccionar y comprender el mundo que nos rodea.





    Individualidad
    La individualidad es una de las características más increíbles del ser humano. Aunque vivimos en el mismo entorno y estamos sujetos a las mismas situaciones, cada ser es único y tiene diferentes visiones acerca de la vida, los desafíos y la forma de enfrentar las situaciones del día a día.

    Por lo tanto, entender tu individualidad es fundamental.

    Por otro lado, también debemos estar abiertos a escuchar nuevas opiniones y desarrollar nuevas perspectivas, una vez que las sugerencias y visiones de los demás pueden ayudar a enriquecer nuestras vidas y aumentar nuestro conocimiento.




    Capacidad
    La capacidad está dentro de nuestras creencias personales. Cuando tenemos las herramientas y habilidades necesarias, todos somos capaces de resolver problemas y alcanzar nuestros objetivos.

    Sin embargo, lo que separa a las personas que están en el mismo nivel de conocimiento y recursos es la creencia de que son o no capaces de realizar una tarea determinada.

    Por lo tanto, dentro del principio de capacidad, debes trabajar la mente y reducir las actitudes negativas e improductivas, evitando las quejas y enfrentando los desafíos con positividad y confianza en tu propio potencial.

    También es necesario analizar el comportamiento de otras personas que hacen parte de tu vida: observar cómo reaccionan y realizan determinadas tareas es una excelente manera de aprender.




    Flexibilidad
    Entre los principios de la Programación Neurolingüística también está la importancia de la flexibilidad. Ser flexible es una forma de poder adaptarse a las necesidades del entorno, sin dejar que tu objetivo general se pierda en el camino.

    Si logras ser flexible en tus pensamientos, comportamientos y acciones, podrás alcanzar el éxito de manera efectiva, además de adaptarte a las adversidades y construir un camino mucho más consistente.




    Cambio
    Al igual que la flexibilidad, la capacidad de cambiar también es esencial.

    En este contexto, es necesario que sepas identificar las dificultades y los desafíos en tus acciones, pensamientos y comportamientos y, a partir de ahí, tener la iniciativa y la sabiduría para cambiar, optimizar tu rendimiento y mejorar tus resultados.

    La verdad es que hay varias formas de realizar una misma tarea, por esa razón, permitirte probar nuevos enfoques y nuevas actitudes puede ser la pieza que falta para mejorar tu forma de interactuar con todos los eventos de tu vida.



